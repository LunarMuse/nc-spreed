<template>
	<form @submit.prevent="handleSubmit">
		<NcTextField ref="searchConversations"
			:value="value"
			:label="placeholderText"
			:show-trailing-button="isFocused"
			trailing-button-icon="close"
			v-on="listeners"
			@focus="handleFocus"
			@blur="handleBlur"
			@update:value="updateValue"
			@trailing-button-click="abortSearch"
			@keypress.enter="handleSubmit">
			<Magnify :size="16" />
		</NcTextField>
	</form>
</template>

<script>
import Magnify from 'vue-material-design-icons/Magnify.vue'

import NcTextField from '@nextcloud/vue/dist/Components/NcTextField.js'

import { EventBus } from '../../../services/EventBus.js'

export default {
	name: 'SearchBox',
	components: {
		NcTextField,
		Magnify,
	},
	props: {
		/**
		 * The placeholder for the input field
		 */
		placeholderText: {
			type: String,
			default: t('spreed', 'Search â€¦'),
		},
		/**
		 * The value of the input field.
		 */
		value: {
			type: String,
			required: true,
		},
		/**
		 * If true, this component displays an 'x' button to abort the search
		 */
		isFocused: {
			type: Boolean,
			default: false,
		},
	},

	emits: ['update:value', 'input', 'submit', 'abort-search'],

	computed: {
		listeners() {
			return Object.assign({}, this.$listeners, {
				focus: this.handleFocus,
				blur: this.handleBlur,
			})
		},

		cancelSearchLabel() {
			return t('spreed', 'Cancel search')
		},
	},

	mounted() {
		this.focusInputIfRoot()
		/**
		 * Listen to routeChange global events and focus on the input
		 */
		EventBus.$on('route-change', this.focusInputIfRoot)
	},
	beforeDestroy() {
		EventBus.$off('route-change', this.focusInputIfRoot)
	},
	methods: {
		updateValue(value) {
			this.$emit('update:value', value)
			this.$emit('input', value)
		},
		// Focus the input field of the searchbox component.
		focusInput() {
			this.$refs.searchConversations.$el.focus()
		},
		// Focuses the input if the current route is root.
		focusInputIfRoot() {
			if (this.$route.name === 'root') {
				this.focusInput()
			}
		},
		/**
		 * When the form is submitted we send this event up in order to allow for example
		 * to select the first search result and trigger a route change in the main view.
		 */
		handleSubmit() {
			this.$emit('submit')
		},
		/**
		 * Emits the abort-search event and re-focuses the input
		 */
		abortSearch() {
			this.$emit('abort-search')
			this.focusInput()
		},

		handleFocus(event) {
			this.$emit('focus', event)
		},
		handleBlur(event) {
			console.log('inner blur')
			if (Array.from(event.relatedTarget.classList).includes('input-field__clear-button')) {
				event.preventDefault()
				this.$refs.searchConversations.$el.querySelector('.input-field__clear-button').addEventListener('blur', (event) => {
					console.log('trailing blur')
					console.log(event)
					// check if focus goes back to native input or outside, and proceed accordingly
				})
			} else {
				this.$emit('blur', event)
			}
		},
	},
}
</script>

